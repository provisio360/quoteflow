export type CountryName = (typeof ISO_3166_COUNTRY_NAMES)[number];

// Lookup keyed by the normalised form (lowercased, whitespace-collapsed) so a
// case-/spacing-variant resolves to its canonical name in O(1).
const BY_NORMALISED: ReadonlyMap<string, CountryName> = new Map(
  ISO_3166_COUNTRY_NAMES.map((name) => [normaliseCountryKey(name), name]),
);

/** Collapse case and internal whitespace for tolerant matching. */
export function normaliseCountryKey(value: string): string {
  return value.trim().replace(/\s+/g, " ").toLowerCase();
}

/**
 * Resolve a free-typed country value to its canonical name, or `null` if it is
 * not in the bundled list. The validator turns `null` into a row error.
 */
export function canonicalCountry(value: string): CountryName | null {
  return BY_NORMALISED.get(normaliseCountryKey(value)) ?? null;
}
